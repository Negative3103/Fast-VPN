import Foundation

enum Symbols: String {
    case plus     = "+"
    case minus    = "-"
    case equal    = "="
    case question = "?"
    case and      = "&"
    case number   = "№"
    case empty    = ""
    case space    = " "
    case comma    = ","
    case point    = "."
    case slash    = "/"
    case doublePoint = ":"
}
